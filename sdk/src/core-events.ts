// ── Sandbox lifecycle events (Microsandbox migration) ──
// See sdk/src/sandbox.ts for the typed report/status shapes.

/** `get_sandbox_status` / `reset_sandbox` response. */
export interface SandboxStatusEvent extends ProtocolMetadata {
  type: "sandbox_status";
  /** Effective sandbox mode. */
  mode: string;
  /** Serialized preflight report (snake_case). */
  report?: Record<string, unknown>;
  /** True when this status follows a `reset_sandbox` command. */
  reset?: boolean;
}

/** Streamed while `prepare_sandbox` downloads runtime/image assets. */
export interface SandboxPrepareProgressEvent extends ProtocolMetadata {
  type: "sandbox_prepare_progress";
  /** Current preparation phase, e.g. "downloading-runtime-and-image". */
  phase: string;
}

/** Terminal success of `prepare_sandbox`. */
export interface SandboxReadyEvent extends ProtocolMetadata {
  type: "sandbox_ready";
  /** Whether the sandbox is now ready to execute commands. */
  ready: boolean;
  /** Serialized preflight report (snake_case). */
  report?: Record<string, unknown>;
}

/** Terminal failure of `prepare_sandbox`. */
export interface SandboxErrorEvent extends ProtocolMetadata {
  type: "sandbox_error";
  /** Human-readable error message (never carries secret values). */
  error: string;
}

