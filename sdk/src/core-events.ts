export type CoreEventType = (typeof CORE_EVENT_TYPES)[number];

export function isKnownCoreEventType(type: string): type is CoreEventType {
  return (CORE_EVENT_TYPES as readonly string[]).includes(type);
}

/** Wire-level core event. Always has `type`; other fields vary by kind. */
export interface ProtocolMetadata {
  protocol_version?: number;
  session_id?: string;
  run_id?: string;
  sequence?: number;
}

export type CoreEvent = { type: string } & ProtocolMetadata & Record<string, unknown>;

export interface RuntimeResource {
  id: number;
  kind: string;
  label: string;
  session_id: string;
  run_id?: string;
  cancelled: boolean;
}

export interface RuntimeStatusEvent extends ProtocolMetadata {
  type: "runtime_status";
  discarded_stale_results: number;
  resources: RuntimeResource[];
  pending_approvals: number;
  pending_asks: number;
  pending_sudos: number;
}

